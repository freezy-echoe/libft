#include "libft.h"

void	ft_striteri(char *s, void (*f) (size_t, char *))
{
	size_t	i;

	if (!s || !f)
		return ;
	i = 0;
	while (s[i])
	{
		(*f)(i, &s[i]);
		i++;
	}
}
